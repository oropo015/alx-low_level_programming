#include "main.h"

/**
 * Perform arithmetic
 * add - addition
 * sub - subtraction
 * mul - Multiplicatio
 * div2 - Addition
 * mod - Modulus
 */

int add(int a, int b)
{
	return a + b;
}
int sub(int a, int b)
{
	return a - b;
}
int mul(int a, int b)
{
	return a * b;
}
int div2(int a, int b)
{
	return a / b;
}
int mod(int a, int b)
{
	return a % b;
}
